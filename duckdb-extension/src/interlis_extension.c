typedef struct {
    char *cmd;         // "models", "topics", "classes", "attributes", "enums"
    char *modeldir;
    char *model;
    char *class;
    char *mapping;     // import mapping mode (e.g. "relational")
    char *mode;        // import mode (e.g. "create", "replace", "append")
} mi_bind_data;

typedef struct {
    char **rows;
    idx_t row_count;
    idx_t current_row;
    int col_count;
} mi_init_data;

static void mi_bind_destroy(void *d) {
    mi_bind_data *bd = (mi_bind_data *)d;
    if (bd) { free(bd->cmd); free(bd->modeldir); free(bd->model); free(bd->class); free(bd->mapping); free(bd->mode); free(bd); }
}

static void mi_init_destroy(void *d) {
    mi_init_data *id = (mi_init_data *)d;
    if (id) {
        for (idx_t i = 0; i < id->row_count; i++) free(id->rows[i]);
        free(id->rows); free(id);
    }
}

static void mi_bind(duckdb_bind_info info, const char *cmd, int ncols,
                     const char **colnames) {
    duckdb_logical_type vt = duckdb_create_logical_type(DUCKDB_TYPE_VARCHAR);
    for (int i = 0; i < ncols; i++)
        duckdb_bind_add_result_column(info, colnames[i], vt);

    char *modeldir = ili_bind_copy_parameter_varchar_or_error(info, 0, "input");
    char *model = ili_bind_copy_named_varchar_or_error(info, "model");
    char *cls = ili_bind_copy_named_varchar_or_error(info, "class");

    mi_bind_data *bd = ili_calloc_or_error_bind(info, 1, sizeof(*bd), "mi_bind_data");
    if (!bd) {
        free(modeldir); free(model); free(cls);
        return;
    }
    bd->cmd = strdup(cmd);
    if (!bd->cmd) {
        duckdb_bind_set_error(info, "Out of memory");
        mi_bind_destroy(bd);
        free(modeldir); free(model); free(cls);
        return;
    }
    bd->modeldir = modeldir;
    bd->model = model;
    bd->class = cls;
    duckdb_bind_set_bind_data(info, bd, mi_bind_destroy);
}

static void mi_init(duckdb_init_info info) {
    if (!ensure_native_ready()) {
        duckdb_init_set_error(info, get_init_error());
        return;
    }
    mi_bind_data *bd = (mi_bind_data *)duckdb_init_get_bind_data(info);
    if (!bd) {
        duckdb_init_set_error(info, "Missing bind data");
        return;
    }

    ili_request req;
    memset(&req, 0, sizeof(req));
    req.struct_size = sizeof(req);
    req.cmd = bd->cmd;
    req.modeldir = bd->modeldir;
    req.model = bd->model;
    req.class_name = bd->class;
    req.max_messages = -1;

    int status = -1;
    char *result = ili_call_struct_str(g_native_model_info, &req, &status);
    if (status != 0 || !result) {
        ili_report_error(info, status, result, "Model info call failed");
        return;
    }

    mi_init_data *id = ili_calloc_or_error_init(info, 1, sizeof(*id), "mi_init_data");
    if (!id) { free(result); return; }

    // Count rows
    const char *p = result;
    while (*p) { id->row_count++; while (*p && *p != '\n') p++; if (*p == '\n') p++; }

    id->rows = ili_malloc_or_error_init(info, id->row_count * sizeof(char*), "model info rows");
    if (!id->rows) { free(result); free(id); return; }
    p = result;
    for (idx_t i = 0; i < id->row_count; i++) {
        const char *start = p; size_t len = 0;
        while (*p && *p != '\n') { p++; len++; }
        id->rows[i] = malloc(len + 1);
        if (!id->rows[i]) {
            duckdb_init_set_error(info, "Out of memory allocating model info row");
            free(result);
            mi_init_destroy(id);
            return;
        }
        memcpy(id->rows[i], start, len);
        id->rows[i][len] = '\0';
        if (*p == '\n') p++;
    }
    free(result);
    duckdb_init_set_init_data(info, id, mi_init_destroy);
}

static void mi_function(duckdb_function_info tfinfo, duckdb_data_chunk output) {
    mi_init_data *id = (mi_init_data *)duckdb_function_get_init_data(tfinfo);
    if (!id || id->current_row >= id->row_count) {
        duckdb_data_chunk_set_size(output, 0); return;
    }
    idx_t count = id->row_count - id->current_row;
    if (count > 1024) count = 1024;

    idx_t col_count = duckdb_data_chunk_get_column_count(output);

    for (idx_t i = 0; i < count; i++) {
        char *row = id->rows[id->current_row + i];
        const char *end = row + strlen(row);
        const char *cursor = row;
        ili_tsv_field field;
        for (idx_t c = 0; c < col_count && ili_tsv_next_field(&cursor, end, &field); c++) {
            duckdb_vector vec = duckdb_data_chunk_get_vector(output, c);
            if (!ili_tsv_assign_varchar(vec, i, &field)) {
                duckdb_function_set_error(tfinfo, "Failed to decode TSV field");
                duckdb_data_chunk_set_size(output, 0);
                return;
            }
        }
    }
    duckdb_data_chunk_set_size(output, count);
    id->current_row += count;
}

