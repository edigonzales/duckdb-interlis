// ---------------------------------------------------------------------------
// Verify that a file's SHA-256 matches the expected hash
// On success returns true. On failure writes error to error_buf and returns false.
// ---------------------------------------------------------------------------
static bool verify_file_hash(const char *path, char *error_buf, size_t error_bufsize) {
    char actual_hash[65];
    if (sha256_file(path, actual_hash) != 0) {
        snprintf(error_buf, error_bufsize,
            "Hash verification failed for '%s': could not read file", path);
        return false;
    }
    if (strcmp(actual_hash, embedded_native_lib_hash) != 0) {
        snprintf(error_buf, error_bufsize,
            "Hash mismatch for '%s': expected %s, got %s",
            path, embedded_native_lib_hash, actual_hash);
        return false;
    }
    return true;
}

// ---------------------------------------------------------------------------
// Check if path is a regular file (not a symlink/junction)
// ---------------------------------------------------------------------------
static bool is_regular_file(const char *path) {
#ifndef _WIN32
    struct stat st;
    if (lstat(path, &st) != 0) return false;
    return S_ISREG(st.st_mode);
#else
    DWORD attr = GetFileAttributesA(path);
    if (attr == INVALID_FILE_ATTRIBUTES) return false;
    if (attr & FILE_ATTRIBUTE_REPARSE_POINT) return false;
    if (attr & FILE_ATTRIBUTE_DIRECTORY) return false;
    return true;
#endif
}

// ---------------------------------------------------------------------------
// Flush and sync a FILE* to disk
// ---------------------------------------------------------------------------
static bool flush_and_sync(FILE *f) {
    if (fflush(f) != 0) return false;
#ifndef _WIN32
    if (fsync(fileno(f)) != 0) return false;
#else
    int fd = _fileno(f);
    if (fd < 0) return false;
    HANDLE h = (HANDLE)_get_osfhandle(fd);
    if (h == INVALID_HANDLE_VALUE) return false;
    if (!FlushFileBuffers(h)) return false;
#endif
    return true;
}

// ---------------------------------------------------------------------------
// Safe extraction: write to temp file, verify hash, atomic rename
// On success returns true. On failure writes error to error_buf.
// If another process already extracted the file (rename fails), verifies
// the existing file's hash and reports success if it matches.
// ---------------------------------------------------------------------------
static bool extract_native_lib_safe(const char *final_path, char *error_buf, size_t error_bufsize) {
    if (embedded_native_lib_size == 0) {
        snprintf(error_buf, error_bufsize,
            "No embedded native library available (size=0)");
        return false;
    }

    // Build temp file path: final_path + ".tmp." + pid
    char temp_path[2048];
#ifdef _WIN32
    int pid = (int)GetCurrentProcessId();
#else
    int pid = (int)getpid();
#endif
    int written = snprintf(temp_path, sizeof(temp_path), "%s.tmp.%d", final_path, pid);
    if (written < 0 || (size_t)written >= sizeof(temp_path)) {
        snprintf(error_buf, error_bufsize,
            "Temp path too long for '%s.tmp.%d'", final_path, pid);
        return false;
    }

    // Write to temp file
    FILE *f = fopen(temp_path, "wb");
    if (!f) {
        char errstr[128];
        ili_strerror(errno, errstr, sizeof(errstr));
        snprintf(error_buf, error_bufsize,
            "Failed to open temp file '%s': %s", temp_path, errstr);
        return false;
    }

    size_t w = fwrite(embedded_native_lib_data, 1, embedded_native_lib_size, f);
    if (w != embedded_native_lib_size) {
        char errstr[128];
        ili_strerror(errno, errstr, sizeof(errstr));
        snprintf(error_buf, error_bufsize,
            "Failed to write temp file '%s': wrote %zu of %zu bytes (%s)",
            temp_path, w, (size_t)embedded_native_lib_size, errstr);
        fclose(f);
        unlink(temp_path);
        return false;
    }

    // Flush and sync
    if (!flush_and_sync(f)) {
        char errstr[128];
        ili_strerror(errno, errstr, sizeof(errstr));
        snprintf(error_buf, error_bufsize,
            "Failed to flush/sync temp file '%s': %s", temp_path, errstr);
        fclose(f);
        unlink(temp_path);
        return false;
    }
    fclose(f);

    // Set executable permission on temp file
#ifndef _WIN32
    if (chmod(temp_path, 0755) != 0) {
        char errstr[128];
        ili_strerror(errno, errstr, sizeof(errstr));
        snprintf(error_buf, error_bufsize,
            "Failed to chmod temp file '%s': %s", temp_path, errstr);
        unlink(temp_path);
        return false;
    }
#endif

    // Verify hash of temp file
    if (!verify_file_hash(temp_path, error_buf, error_bufsize)) {
        unlink(temp_path);
        return false;
    }

    // Atomic rename (or handle race condition if another process was faster)
    if (rename(temp_path, final_path) == 0) {
        return true;
    }

    // Rename failed - another process may have created the file concurrently
    unlink(temp_path);

    if (!file_exists(final_path)) {
        char errstr[128];
        ili_strerror(errno, errstr, sizeof(errstr));
        snprintf(error_buf, error_bufsize,
            "Failed to rename '%s' to '%s' and target does not exist: %s",
            temp_path, final_path, errstr);
        return false;
    }

    // Verify the existing file's hash
    if (!verify_file_hash(final_path, error_buf, error_bufsize)) {
        unlink(final_path);
        return false;
    }

    // Another process extracted it correctly - success
    return true;
}

// ---------------------------------------------------------------------------
// Library path resolution with hash verification and atomic extraction
// Sets g_path_error on failure.
// ---------------------------------------------------------------------------
static char g_path_error[2048];

static const char *resolve_native_lib_path(void) {
    g_path_error[0] = '\0';

    // 1. Explicit environment override (development only)
    const char *env = getenv("DUCKDB_ILI_NATIVE_LIB");
    if (env && env[0]) {
        ILI_DEBUG_LOG("Native library path from DUCKDB_ILI_NATIVE_LIB: %s", env);
        return env;
    }

    // 2. Hashed extension cache: {home}/.duckdb/extensions/{abi}/{platform}/{hashed_filename}
    static char cache_dir[512];
    build_cache_dir(cache_dir, sizeof(cache_dir));
    if (cache_dir[0]) {
        static char cache_path[2048];
        cache_file_name(cache_path, sizeof(cache_path));

        static char cache_full_path[2048];
        int n = snprintf(cache_full_path, sizeof(cache_full_path), "%s/%s", cache_dir, cache_path);
        if (n >= 0 && (size_t)n < sizeof(cache_full_path)) {

            // Check if cached file already exists
            if (file_exists(cache_full_path)) {
                if (!is_regular_file(cache_full_path)) {
                    snprintf(g_path_error, sizeof(g_path_error),
                        "Cache file '%s' is not a regular file (symlink/junction rejected)", cache_full_path);
                    ILI_DEBUG_LOG("Cache file rejected (not a regular file): %s", cache_full_path);
                    unlink(cache_full_path);
                } else if (verify_file_hash(cache_full_path, g_path_error, sizeof(g_path_error))) {
                    ILI_DEBUG_LOG("Native library cache HIT: %s", cache_full_path);
                    return cache_full_path;
                } else {
                    // Hash mismatch - delete corrupt file and re-extract
                    ILI_DEBUG_LOG("Cache file hash mismatch, re-extracting: %s", cache_full_path);
                    unlink(cache_full_path);
                }
            } else {
                ILI_DEBUG_LOG("Native library cache MISS: %s", cache_full_path);
            }

            // Extract embedded library
            if (make_dir_recursive_safe(cache_dir) == 0) {
                ILI_DEBUG_LOG("Extracting native library (%zu bytes) to: %s", embedded_native_lib_size, cache_full_path);
                if (extract_native_lib_safe(cache_full_path, g_path_error, sizeof(g_path_error))) {
                    ILI_DEBUG_LOG("Native library extraction successful");
                    return cache_full_path;
                }
                ILI_DEBUG_LOG("Native library extraction FAILED: %s", g_path_error);
            } else {
                char errstr[128];
                ili_strerror(errno, errstr, sizeof(errstr));
                snprintf(g_path_error, sizeof(g_path_error),
                    "Failed to create cache directory '%s': %s", cache_dir, errstr);
            }
        }
    }

    // 2b. Temp directory fallback (when home cache not writable, e.g. CI)
    {
        char tmp_dir[512];
        get_temp_dir(tmp_dir, sizeof(tmp_dir));
        if (tmp_dir[0]) {
            static char tmp_cache_path[2048];
            cache_file_name(tmp_cache_path, sizeof(tmp_cache_path));

            static char tmp_full_path[2048];
            int n = snprintf(tmp_full_path, sizeof(tmp_full_path), "%s/%s", tmp_dir, tmp_cache_path);
            if (n >= 0 && (size_t)n < sizeof(tmp_full_path)) {
                if (file_exists(tmp_full_path)) {
                    if (verify_file_hash(tmp_full_path, g_path_error, sizeof(g_path_error))) {
                        ILI_DEBUG_LOG("Native library temp cache HIT: %s", tmp_full_path);
                        return tmp_full_path;
                    } else {
                        unlink(tmp_full_path);
                    }
                }

                if (make_dir_recursive_safe(tmp_dir) == 0) {
                    ILI_DEBUG_LOG("Extracting native library to temp: %s", tmp_full_path);
                    if (extract_native_lib_safe(tmp_full_path, g_path_error, sizeof(g_path_error))) {
                        ILI_DEBUG_LOG("Native library temp extraction successful");
                        return tmp_full_path;
                    }
                }
            }
        }
    }

    // 3. Fallback: local dev paths (debug builds only)
#ifndef NDEBUG
    static const char *fallbacks[] = {
        "build/native/current/" NATIVE_LIB_NAME,
        "../java/ili-native/build/native/" NATIVE_LIB_NAME,
        "../../java/ili-native/build/native/" NATIVE_LIB_NAME,
        NULL
    };
    for (int i = 0; fallbacks[i]; i++) {
        if (file_exists(fallbacks[i])) {
            ILI_DEBUG_LOG("Native library found at dev fallback path: %s", fallbacks[i]);
            return fallbacks[i];
        }
    }
#endif

    // No library found
    if (!g_path_error[0]) {
        snprintf(g_path_error, sizeof(g_path_error),
            "Native library not found. Set DUCKDB_ILI_NATIVE_LIB or run scripts/build-native.sh");
    }
    return NULL;
}

// ---------------------------------------------------------------------------
// Thread-safe native library initialization (called exactly once)
// ---------------------------------------------------------------------------
static bool init_native_library_locked(void) {
    const char *lib_path = resolve_native_lib_path();
    if (!lib_path) {
        if (g_path_error[0]) {
            g_init_error = strdup(g_path_error);
        } else {
            g_init_error = strdup(
                "Native library not found. Set DUCKDB_ILI_NATIVE_LIB or run scripts/build-native.sh");
        }
        return false;
    }

    g_native_handle = dlopen(lib_path, RTLD_LAZY);
    if (!g_native_handle) {
        size_t len = strlen(lib_path) + 256;
        g_init_error = malloc(len);
        if (g_init_error) snprintf(g_init_error, len,
            "Failed to load native library '%s': %s", lib_path, dlerror());
        return false;
    }

    // Resolve lifecycle functions
    g_create_isolate = (graal_create_isolate_fn_t)dlsym(g_native_handle, "graal_create_isolate");
    g_attach_thread = (graal_attach_thread_fn_t)dlsym(g_native_handle, "graal_attach_thread");
    g_detach_thread = (graal_detach_thread_fn_t)dlsym(g_native_handle, "graal_detach_thread");
    g_tear_down = (graal_tear_down_isolate_fn_t)dlsym(g_native_handle, "graal_tear_down_isolate");

    if (!g_create_isolate || !g_attach_thread || !g_detach_thread || !g_tear_down) {
        size_t len = strlen(lib_path) + 128;
        g_init_error = malloc(len);
        if (g_init_error) snprintf(g_init_error, len,
            "Failed to resolve GraalVM lifecycle symbols in '%s'", lib_path);
        dlclose(g_native_handle);
        g_native_handle = NULL;
        return false;
    }

    // Resolve ili_get_api — single entry point for ABI handshake
    typedef int (*ili_get_api_fn_t)(graal_isolatethread_t*, uint32_t, char**);
    ili_get_api_fn_t get_api = (ili_get_api_fn_t)dlsym(g_native_handle, "ili_get_api");

    // Also resolve free_string early so we can free the ABI payload
    g_native_free = (native_free_fn)dlsym(g_native_handle, "ili_free_string");
    if (!g_native_free) {
        size_t len = strlen(lib_path) + 128;
        g_init_error = malloc(len);
        if (g_init_error) snprintf(g_init_error, len,
            "Native library '%s' is incompatible (missing ili_free_string)", lib_path);
        dlclose(g_native_handle);
        g_native_handle = NULL;
        return false;
    }
    if (!get_api) {
        size_t len = strlen(lib_path) + 128;
        g_init_error = malloc(len);
        if (g_init_error) snprintf(g_init_error, len,
            "Native library '%s' is incompatible (missing ili_get_api)", lib_path);
        dlclose(g_native_handle);
        g_native_handle = NULL;
        return false;
    }

    // Create isolate
    graal_isolatethread_t *init_thread = NULL;
    int rc = g_create_isolate(NULL, &g_isolate, &init_thread);
    if (rc != 0 || !g_isolate) {
        size_t len = 128;
        g_init_error = malloc(len);
        if (g_init_error) snprintf(g_init_error, len,
            "Failed to create GraalVM isolate (code=%d)", rc);
        dlclose(g_native_handle);
        g_native_handle = NULL;
        return false;
    }

    // ABI handshake: validate version and capabilities
    memset(&g_handshake, 0, sizeof(g_handshake));
    g_handshake.struct_size = sizeof(g_handshake);
    char *abi_payload = NULL;
    rc = get_api(init_thread, ILI_NATIVE_ABI_VERSION, &abi_payload);
    if (rc != 0 || !abi_payload) {
        size_t len = 512;
        g_init_error = malloc(len);
        if (abi_payload) {
            if (g_init_error) snprintf(g_init_error, len,
                "ABI handshake failed (rc=%d): %.480s", rc, abi_payload);
            g_native_free(init_thread, abi_payload);
        } else {
            if (g_init_error) snprintf(g_init_error, len,
                "ABI handshake failed: requested version %d, library returned code %d",
                ILI_NATIVE_ABI_VERSION, rc);
        }
        g_tear_down(init_thread);
        g_isolate = NULL;
        dlclose(g_native_handle);
        g_native_handle = NULL;
        return false;
    }

    // Parse ABI payload with toleration for whitespace and field order.
    // Expected format: {"abi_version":1,"capabilities":4095}
    {
        int parsed_version = 0;
        unsigned long long parsed_caps = 0;
        int n = 0;

        int n_matched = sscanf(abi_payload,
            " {\"abi_version\": %d ,\"capabilities\": %llu }%n",
            &parsed_version, &parsed_caps, &n);

        if (n_matched != 2 || parsed_version < 1 || n == 0) {
            size_t len = strlen(abi_payload) + 256;
            g_init_error = malloc(len);
            if (g_init_error) snprintf(g_init_error, len,
                "ABI handshake returned malformed payload (matched=%d, n=%d): %.480s",
                n_matched, n, abi_payload);
            g_native_free(init_thread, abi_payload);
            g_tear_down(init_thread);
            g_isolate = NULL;
            dlclose(g_native_handle);
            g_native_handle = NULL;
            return false;
        }
        g_handshake.abi_version = (uint32_t)parsed_version;
        g_handshake.capabilities = (uint64_t)parsed_caps;
        g_native_free(init_thread, abi_payload);
    }

    // Validate the handshake result
    if (g_handshake.abi_version != ILI_NATIVE_ABI_VERSION) {
        size_t len = 512;
        g_init_error = malloc(len);
        if (g_init_error) snprintf(g_init_error, len,
            "ABI version mismatch: got %u, expected %u",
            g_handshake.abi_version, ILI_NATIVE_ABI_VERSION);
        goto cleanup_handshake_fail;
    }

    // Check required capabilities
    uint64_t missing = ILI_CAP_REQUIRED_MASK & ~g_handshake.capabilities;
    if (missing != 0) {
        size_t len = 512;
        g_init_error = malloc(len);
        if (g_init_error) snprintf(g_init_error, len,
            "Missing required capabilities: 0x%016llx (have 0x%016llx, need 0x%016llx)",
            (unsigned long long)missing,
            (unsigned long long)g_handshake.capabilities,
            (unsigned long long)ILI_CAP_REQUIRED_MASK);
        goto cleanup_handshake_fail;
    }

    // ABI handshake successful. Now resolve individual API function pointers.
    g_native_version = (native_version_fn)dlsym(g_native_handle, "ili_native_version");
    g_native_validate = (native_struct_fn)dlsym(g_native_handle, "ili_native_validate");
    g_native_validate_tsv = (native_struct_fn)dlsym(g_native_handle, "ili_native_validate_tsv");
    g_native_model_info = (native_struct_fn)dlsym(g_native_handle, "ili_native_model_info");
    g_native_read_xtf = (native_struct_fn)dlsym(g_native_handle, "ili_native_read_xtf");
    g_native_read_xtf_class = (native_struct_fn)dlsym(g_native_handle, "ili_native_read_xtf_class");
    g_native_read_xtf_class_schema = (native_struct_fn)dlsym(g_native_handle, "ili_native_read_xtf_class_schema");
    g_native_read_xtf_structures = (native_struct_fn)dlsym(g_native_handle, "ili_native_read_xtf_structures");
    g_native_read_xtf_association = (native_struct_fn)dlsym(g_native_handle, "ili_native_read_xtf_association");
    g_native_read_xtf_association_schema = (native_struct_fn)dlsym(g_native_handle, "ili_native_read_xtf_association_schema");
    g_native_generate_import_sql = (native_struct_fn)dlsym(g_native_handle, "ili_native_generate_import_sql");
    g_native_free = (native_free_fn)dlsym(g_native_handle, "ili_free_string");

    if (!g_native_version || !g_native_validate || !g_native_validate_tsv || !g_native_model_info
        || !g_native_read_xtf || !g_native_read_xtf_class || !g_native_read_xtf_class_schema
        || !g_native_read_xtf_structures || !g_native_read_xtf_association
        || !g_native_read_xtf_association_schema || !g_native_generate_import_sql || !g_native_free) {
        size_t len = strlen(lib_path) + 128;
        g_init_error = malloc(len);
        if (g_init_error) snprintf(g_init_error, len,
            "Failed to resolve ILI API symbols in '%s' (ABI handshake passed but symbols missing)", lib_path);
        goto cleanup_handshake_fail;
    }

    // Check for typed class scan capability (bit 12)
    // This is optional — if present, we use v2 typed path with GEOMETRY columns.
    // If absent, we fall back to v1 VARCHAR-only path.
    if (g_handshake.capabilities & ILI_CAP_TYPED_CLASS_SCAN) {
        g_native_read_xtf_class_schema_v2 =
            (native_struct_fn)dlsym(g_native_handle, "ili_native_read_xtf_class_schema_v2");
        g_native_read_xtf_class_v2 =
            (native_struct_fn)dlsym(g_native_handle, "ili_native_read_xtf_class_v2");
        if (g_native_read_xtf_class_schema_v2 && g_native_read_xtf_class_v2) {
            g_has_typed_class_scan = true;
            ILI_DEBUG_LOG("Typed class scan v2 enabled (ILI_CAP_TYPED_CLASS_SCAN present)");
        } else {
            ILI_DEBUG_LOG("ILI_CAP_TYPED_CLASS_SCAN claimed but v2 symbols not found; using v1 fallback");
        }
    } else {
        ILI_DEBUG_LOG("Typed class scan v2 not available (ILI_CAP_TYPED_CLASS_SCAN absent); using v1 VARCHAR path");
    }

    // Detach the init thread since DuckDB will attach its own threads
    g_detach_thread(init_thread);

    ILI_DEBUG_LOG("Native library initialized: ABI v%u, capabilities=0x%016llx, ext=%s",
        g_handshake.abi_version,
        (unsigned long long)g_handshake.capabilities,
        EXT_VERSION);
    return true;

cleanup_handshake_fail:
    g_tear_down(init_thread);
    g_isolate = NULL;
    dlclose(g_native_handle);
    g_native_handle = NULL;
    return false;
}

// Thread-safe initialization. Returns true if native library is ready.
// Stores persistent error in g_init_error on failure; does NOT retry.
bool ensure_native_ready(void) {
    ili_mutex_lock(&g_init_lock);

    if (g_init_done) {
        ili_mutex_unlock(&g_init_lock);
        return true;
    }
    if (g_init_error) {
        // Already attempted and failed - don't retry
        ili_mutex_unlock(&g_init_lock);
        return false;
    }

    if (init_native_library_locked()) {
        g_init_done = true;
        ili_mutex_unlock(&g_init_lock);
        return true;
    }

    // g_init_error was set by init_native_library_locked()
    ili_mutex_unlock(&g_init_lock);
    return false;
}

// Returns the initialization error message, or a default.
const char *get_init_error(void) {
    return g_init_error ? g_init_error : "Native library initialization failed";
}

// ---------------------------------------------------------------------------
// Shutdown (for manual use in tests; not called automatically by DuckDB)
// NOTE: DuckDB does not provide a reliable extension-unload lifecycle.
// Cleanup (isolate teardown, dlclose) happens at process exit via OS.
// ---------------------------------------------------------------------------
static void shutdown_native_library(void) {
    ili_mutex_lock(&g_init_lock);
    if (!g_init_done) {
        ili_mutex_unlock(&g_init_lock);
        return;
    }

    graal_isolatethread_t *thread = NULL;
    if (g_attach_thread(g_isolate, &thread) == 0 && thread) {
        g_tear_down(thread);
    }
    if (g_native_handle) {
        dlclose(g_native_handle);
        g_native_handle = NULL;
    }
    g_isolate = NULL;
    g_native_version = NULL;
    g_native_validate = NULL;
    g_native_validate_tsv = NULL;
    g_native_model_info = NULL;
    g_native_read_xtf = NULL;
    g_native_read_xtf_class = NULL;
    g_native_read_xtf_class_schema = NULL;
    g_native_read_xtf_structures = NULL;
    g_native_read_xtf_association = NULL;
    g_native_read_xtf_association_schema = NULL;
    g_native_generate_import_sql = NULL;
    g_native_read_xtf_class_schema_v2 = NULL;
    g_native_read_xtf_class_v2 = NULL;
    g_native_free = NULL;
    g_has_typed_class_scan = false;
    memset(&g_handshake, 0, sizeof(g_handshake));
    g_create_isolate = NULL;
    g_attach_thread = NULL;
    g_detach_thread = NULL;
    g_tear_down = NULL;
    free(g_init_error);
    g_init_error = NULL;
    g_init_done = false;
    ili_mutex_unlock(&g_init_lock);
}

// ---------------------------------------------------------------------------
// Consolidated Native Call Helpers
//
// These helpers combine: lock -> attach -> call -> copy -> free -> detach -> unlock
// in a single cycle. All Java calls are serialized by g_java_lock.
// Results are returned as C-allocated (strdup) strings; caller must free().
// GraalVM-allocated strings are freed inside the same attach/detach window.
// ---------------------------------------------------------------------------

// No-input call (e.g. ili_native_version)
// Returns C-allocated copy of result, or NULL. Sets *out_status.
static char *ili_call_str(int (*fn)(graal_isolatethread_t *, char **), int *out_status) {
    *out_status = -1;
    if (!g_isolate || !fn) return NULL;

    ili_mutex_lock(&g_java_lock);

    graal_isolatethread_t *thread = NULL;
    if (g_attach_thread(g_isolate, &thread) != 0 || !thread) {
        ili_mutex_unlock(&g_java_lock);
        return NULL;
    }

    char *native_str = NULL;
    int rc = fn(thread, &native_str);
    *out_status = rc;

    char *result = NULL;
    if (rc == 0 && native_str) {
        result = strdup(native_str);  // C-owned copy
    } else if (native_str) {
        result = strdup(native_str);  // Error payload: C-owned copy
    }

    if (native_str) g_native_free(thread, native_str);
    g_detach_thread(thread);
    ili_mutex_unlock(&g_java_lock);
    return result;
}

// With-string-input call (e.g. ili_native_echo)
// Returns C-allocated copy of result, or NULL. Sets *out_status.
static char *ili_call_input_str(
        int (*fn)(graal_isolatethread_t *, char *, char **),
        const char *input, int *out_status) {
    *out_status = -1;
    if (!g_isolate || !fn) return NULL;

    ili_mutex_lock(&g_java_lock);

    graal_isolatethread_t *thread = NULL;
    if (g_attach_thread(g_isolate, &thread) != 0 || !thread) {
        ili_mutex_unlock(&g_java_lock);
        return NULL;
    }

    char *native_str = NULL;
    int rc = fn(thread, (char *)input, &native_str);
    *out_status = rc;

    char *result = NULL;
    if (rc == 0 && native_str) {
        result = strdup(native_str);
    } else if (native_str) {
        result = strdup(native_str);
    }

    if (native_str) g_native_free(thread, native_str);
    g_detach_thread(thread);
    ili_mutex_unlock(&g_java_lock);
    return result;
}

// With-ili_request call (most API functions)
// Returns C-allocated copy of result, or NULL. Sets *out_status.
// Non-static so ili_typed_scan.c can use it.
char *ili_call_struct_str(
        native_struct_fn fn, const ili_request *req, int *out_status) {
    *out_status = -1;
    if (!g_isolate || !fn) return NULL;

    ili_mutex_lock(&g_java_lock);

    graal_isolatethread_t *thread = NULL;
    if (g_attach_thread(g_isolate, &thread) != 0 || !thread) {
        ili_mutex_unlock(&g_java_lock);
        return NULL;
    }

    char *native_str = NULL;
    int rc = fn(thread, (ili_request *)req, &native_str);
    *out_status = rc;

    char *result = NULL;
    if (rc == 0 && native_str) {
        result = strdup(native_str);
    } else if (native_str) {
        result = strdup(native_str);
    }

    if (native_str) g_native_free(thread, native_str);
    g_detach_thread(thread);
    ili_mutex_unlock(&g_java_lock);
    return result;
}

// ---------------------------------------------------------------------------
// Error extraction
// ---------------------------------------------------------------------------
char *extract_error_message(const char *json_payload) {
    if (!json_payload || json_payload[0] != '{') return NULL;

    const char *msg_start = strstr(json_payload, "\"message\":\"");
    if (!msg_start) return NULL;

    msg_start += 11;
    const char *msg_end = msg_start;
    while (*msg_end && *msg_end != '"') {
        if (*msg_end == '\\' && *(msg_end + 1)) msg_end++;
        msg_end++;
    }

    size_t len = (size_t)(msg_end - msg_start);
    if (len == 0) return NULL;

    char *result = malloc(len + 1);
    if (!result) return NULL;

    char *dst = result;
    const char *src = msg_start;
    while (src < msg_end) {
        if (*src == '\\' && (src + 1) < msg_end) {
            src++;
            switch (*src) {
                case 'n': *dst++ = '\n'; break;
                case 'r': *dst++ = '\r'; break;
                case 't': *dst++ = '\t'; break;
                case '"': *dst++ = '"'; break;
                case '\\': *dst++ = '\\'; break;
                default: *dst++ = *src; break;
            }
        } else {
            *dst++ = *src;
        }
        src++;
    }
    *dst = '\0';

    const char *detail_start = strstr(json_payload, "\"detail\":\"");
    if (detail_start && !strstr(detail_start, result)) {
        detail_start += 10;
        const char *detail_end = detail_start;
        while (*detail_end && *detail_end != '"') {
            if (*detail_end == '\\' && *(detail_end + 1)) detail_end++;
            detail_end++;
        }
        size_t detail_len = (size_t)(detail_end - detail_start);
        if (detail_len > 0 && detail_len < 256) {
            size_t cur = strlen(result);
            char *merged = malloc(cur + 3 + detail_len + 1);
            if (merged) {
                memcpy(merged, result, cur);
                memcpy(merged + cur, ": ", 2);
                memcpy(merged + cur + 2, detail_start, detail_len);
                merged[cur + 2 + detail_len] = '\0';
                free(result);
                result = merged;
            }
        }
    }

    return result;
}

// Report a failed native call to DuckDB. Payload is C-allocated (must free()).
void ili_report_error(duckdb_init_info info, int status, char *payload, const char *fallback) {
    char *msg = extract_error_message(payload);
    duckdb_init_set_error(info, msg ? msg : (payload ? payload : fallback));
    free(msg);
    free(payload);
}

// Report a failed native schema/bind call to DuckDB. Payload is C-allocated (must free()).
void ili_report_bind_error(duckdb_bind_info info, int status, char *payload, const char *fallback) {
    if (payload) {
        char *msg = extract_error_message(payload);
        duckdb_bind_set_error(info, msg ? msg : payload);
        free(msg);
    } else {
        duckdb_bind_set_error(info, fallback);
    }
    free(payload);
}

