// ---------------------------------------------------------------------------
// Helper: recursive mkdir with safe permissions (0700)
// ---------------------------------------------------------------------------
static int make_dir_recursive_safe(const char *path) {
    size_t len = strlen(path);
    char *tmp = malloc(len + 1);
    if (!tmp) return -1;
    memcpy(tmp, path, len + 1);
    for (char *p = tmp + 1; *p; p++) {
        if (*p == '/' || *p == '\\') {
            char saved = *p;
            *p = '\0';
#ifdef _WIN32
            if (_mkdir(tmp) != 0 && errno != EEXIST) {
                free(tmp);
                return -1;
            }
#else
            if (mkdir(tmp, 0700) != 0 && errno != EEXIST) {
                free(tmp);
                return -1;
            }
#endif
            *p = saved;
        }
    }
#ifdef _WIN32
    if (_mkdir(tmp) != 0 && errno != EEXIST) {
        free(tmp);
        return -1;
    }
#else
    if (mkdir(tmp, 0700) != 0 && errno != EEXIST) {
        free(tmp);
        return -1;
    }
#endif
    free(tmp);
    return 0;
}

// ---------------------------------------------------------------------------
// Build DuckDB extension cache directory path
// ---------------------------------------------------------------------------
static void build_cache_dir(char *buf, size_t bufsize) {
    const char *home = getenv("HOME");
#ifdef _WIN32
    if (!home) home = getenv("USERPROFILE");
#endif
    if (home) {
        snprintf(buf, bufsize, "%s/.duckdb/extensions/%s/%s",
            home, ILI_ABI_VERSION, ILI_DUCKDB_PLATFORM);
    } else {
        buf[0] = '\0';
    }
}

static void get_temp_dir(char *buf, size_t bufsize) {
    const char *tmp = NULL;
#ifdef _WIN32
    tmp = getenv("TEMP");
    if (!tmp) tmp = getenv("TMP");
#else
    tmp = getenv("TMPDIR");
    if (!tmp) tmp = "/tmp";
#endif
    if (tmp) {
        snprintf(buf, bufsize, "%s/duckdb-interlis-natives", tmp);
    } else {
        buf[0] = '\0';
    }
}

