typedef struct {
    char *sql_script;
    char *cursor;       // current position in sql_script
} gen_sql_init_data;

static void gen_sql_init_destroy(void *d) {
    gen_sql_init_data *id = (gen_sql_init_data *)d;
    if (id) { free(id->sql_script); free(id); }
}

static void gen_sql_bind(duckdb_bind_info info) {
    duckdb_logical_type vt = duckdb_create_logical_type(DUCKDB_TYPE_VARCHAR);
    duckdb_bind_add_result_column(info, "sql_statement", vt);
    duckdb_destroy_logical_type(&vt);

    char *input = ili_bind_copy_parameter_varchar_or_error(info, 0, "input");
    char *schema_name = ili_bind_copy_named_varchar_or_error(info, "schema");
    char *modeldir = ili_bind_copy_named_varchar_or_error(info, "modeldir");
    char *mapping = ili_bind_copy_named_varchar_or_error(info, "mapping");
    char *mode = ili_bind_copy_named_varchar_or_error(info, "mode");

    mi_bind_data *bd = ili_calloc_or_error_bind(info, 1, sizeof(*bd), "mi_bind_data");
    if (!bd) {
        free(input); free(schema_name); free(modeldir); free(mapping); free(mode);
        return;
    }
    bd->cmd = strdup("import");
    if (!bd->cmd) { duckdb_bind_set_error(info, "Out of memory"); mi_bind_destroy(bd); free(input); free(schema_name); free(modeldir); free(mapping); free(mode); return; }
    bd->modeldir = modeldir;
    bd->model = input;
    bd->class = schema_name;
    bd->mapping = mapping;
    bd->mode = mode;
    duckdb_bind_set_bind_data(info, bd, mi_bind_destroy);
}

static void gen_sql_init_func(duckdb_init_info info) {
    if (!ensure_native_ready()) {
        duckdb_init_set_error(info, get_init_error());
        return;
    }
    mi_bind_data *bd = (mi_bind_data *)duckdb_init_get_bind_data(info);
    if (!bd || !bd->model || !bd->class) {
        duckdb_init_set_error(info, "Missing input or schema");
        return;
    }

    ili_request req;
    memset(&req, 0, sizeof(req));
    req.struct_size = sizeof(req);
    req.input = bd->model;
    req.schema = bd->class;
    req.modeldir = bd->modeldir;
    req.mapping = bd->mapping ? bd->mapping : "relational";
    req.mode = bd->mode ? bd->mode : "create";
    req.max_messages = -1;

    int status = -1;
    char *result = ili_call_struct_str(g_native_generate_import_sql, &req, &status);
    if (status != 0 || !result) {
        ili_report_error(info, status, result, "Native import call failed");
        return;
    }

    ILI_DEBUG_LOG("Import SQL payload: %zu bytes", strlen(result));

    gen_sql_init_data *id = ili_calloc_or_error_init(info, 1, sizeof(*id), "gen_sql_init_data");
    if (!id) { free(result); return; }
    id->sql_script = result;  // Already C-allocated by ili_call_struct_str (strdup)
    id->cursor = id->sql_script;
    duckdb_init_set_init_data(info, id, gen_sql_init_destroy);
}

static void gen_sql_function(duckdb_function_info tfinfo, duckdb_data_chunk output) {
    gen_sql_init_data *id = (gen_sql_init_data *)duckdb_function_get_init_data(tfinfo);
    if (!id || !id->sql_script || !id->cursor || !*id->cursor) {
        duckdb_data_chunk_set_size(output, 0); return;
    }

    duckdb_vector sql_vec = duckdb_data_chunk_get_vector(output, 0);
    duckdb_vector_ensure_validity_writable(sql_vec);
    uint64_t *validity = duckdb_vector_get_validity(sql_vec);

    char *data = id->cursor;
    idx_t row_count = 0;
    while (*data && row_count < 1024) {
        char *nl = strchr(data, '\n');
        idx_t len = nl ? (idx_t)(nl - data) : strlen(data);
        if (len > 0) {
            duckdb_validity_set_row_valid(validity, row_count);
            duckdb_vector_assign_string_element_len(sql_vec, row_count, data, len);
            row_count++;
        }
        if (nl) data = nl + 1;
        else {
            data += len;
            break;
        }
    }
    id->cursor = data;
    duckdb_data_chunk_set_size(output, row_count);
}

